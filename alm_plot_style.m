% custom_plot_style.m
function custom_plot_style()
    % Set default properties for MATLAB plots
    set(0, 'DefaultLineLineWidth', 2);  % Thicker lines
    set(0, 'DefaultAxesFontSize', 14);  % Larger font size for axes labels
    set(0, 'DefaultAxesFontWeight', 'bold');  % Bold font for axes labels
    set(0, 'DefaultAxesTitleFontSizeMultiplier', 1.2);  % Larger title font
    set(0, 'DefaultAxesLabelFontSizeMultiplier', 1.2);  % Larger labels font
    set(0, 'DefaultTextFontSize', 14);  % Larger font for text
    set(0, 'DefaultTextFontWeight', 'bold');  % Bold font for text
    set(0, 'DefaultFigureColor', 'w');  % White background for figures
    set(0, 'DefaultAxesLineWidth', 1.5);  % Thicker axes lines
    set(0, 'DefaultAxesBox', 'on');  % Box around axes
    set(0, 'DefaultLegendFontSize', 14);  % Larger font size for legends
end
